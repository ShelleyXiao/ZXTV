    public static long getFileLeng(ArrayList<FileInfo> fileInfoList) {
        long num = 0;
        for (int i = 0; i < fileInfoList.size(); i++) {
            if (fileInfoList.get(i).IsDir) {
                long tmp = getFolderSize(new File(fileInfoList.get(i).filePath));
                num = num + tmp;
            } else {
                num += fileInfoList.get(i).fileSize;
            }
        }
        return num;
    }

    public static int getfilenum(ArrayList<FileInfo> fileInfoList) {
        int num = 0;
        for (int i = 0; i < fileInfoList.size(); i++) {
            if (fileInfoList.get(i).IsDir) {
                num = num + getDirfilenum(new File(fileInfoList.get(i).filePath));
            } else {
                num++;
            }
        }
        return num;
    }

    public static int getDirfilenum(File dir) {
        File[] file = dir.listFiles();
        int tmp_num = 0;
        for (int i = 0; i < file.length; i++) {
            if (file[i].isFile())
                tmp_num++;
            else
                tmp_num = tmp_num + getDirfilenum(file[i]);
        }
        return tmp_num;
    }

    public static Uri getMediaUriFromFilename(String filename) {
        String extString = getExtFromFilename(filename);
        String volumeName = "external";
        FileCategoryHelper.FileCategory fileCategoryType = FileCategoryHelper.fileExtCategoryType.get(extString);

        Uri uri = null;
        if (fileCategoryType == FileCategoryHelper.FileCategory.MUSIC) {
            uri = MediaStore.Audio.Media.getContentUri(volumeName);
        } else if (fileCategoryType == FileCategoryHelper.FileCategory.PICTURE) {
            uri = MediaStore.Images.Media.getContentUri(volumeName);
        } else if (fileCategoryType == FileCategoryHelper.FileCategory.VIDEO) {
            uri = MediaStore.Video.Media.getContentUri(volumeName);
        } else {
            uri = MediaStore.Files.getContentUri(volumeName);
        }
        return uri;
    }


